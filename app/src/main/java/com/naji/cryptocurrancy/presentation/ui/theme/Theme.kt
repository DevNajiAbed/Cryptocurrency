package com.naji.cryptocurrancy.presentation.ui.theme

import androidx.compose.material3.MaterialTheme
import androidx.compose.material3.darkColorScheme
import androidx.compose.material3.lightColorScheme
import androidx.compose.runtime.Composable
import androidx.compose.ui.graphics.Color
import com.naji.cryptocurrancy.presentation.theme.ColorPrimary
import com.naji.cryptocurrancy.presentation.theme.DarkGray
import com.naji.cryptocurrancy.presentation.theme.MediumGray
import com.naji.cryptocurrancy.presentation.theme.Shapes
import com.naji.cryptocurrancy.presentation.theme.TextWhite

private val DarkColorPalette = darkColorScheme(
    primary = ColorPrimary,
    background = DarkGray,
    onBackground = TextWhite,
    onPrimary = DarkGray
)

private val LightColorPalette = lightColorScheme(
    primary = ColorPrimary,
    background = Color.White,
    onBackground = MediumGray,
    onPrimary = DarkGray
)

@Composable
fun CryptoCurrencyTheme(darkTheme: Boolean = true, content: @Composable() () -> Unit) {
    val colors = if (darkTheme) {
        DarkColorPalette
    } else {
        LightColorPalette
    }

    MaterialTheme(
        colorScheme = colors,
        typography = Typography,
        shapes = Shapes,
        content = content
    )
}